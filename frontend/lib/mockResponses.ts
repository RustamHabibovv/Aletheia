import { AnalysisResult, ToolId } from "./types";

function rand(min: number, max: number) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

export async function getMockResponse(
  toolId: ToolId,
  input: string
): Promise<{ text: string; analysis: AnalysisResult }> {
  await new Promise((r) => setTimeout(r, 1200 + Math.random() * 800));

  switch (toolId) {
    case "general":
      return generalResponse(input);
    case "image-detection":
      return imageResponse(input);
    case "text-detection":
      return textResponse(input);
    case "video-detection":
      return videoResponse(input);
    case "fact-check":
      return factCheckResponse(input);
    case "bot-detection":
      return botResponse(input);
  }
}

function generalResponse(input: string): { text: string; analysis: AnalysisResult } {
  const outcomes = ["ai-generated", "authentic", "likely-false", "likely-true", "unverified"] as const;
  const verdict = outcomes[Math.floor(Math.random() * outcomes.length)];
  const confidence = rand(63, 96);
  const riskMap = {
    "ai-generated": "high", authentic: "low", "likely-false": "high", "likely-true": "low", unverified: "medium",
  } as const;

  const summaries: Record<typeof verdict, string> = {
    "ai-generated": "Content shows strong indicators of being synthetically generated or manipulated.",
    authentic: "No significant indicators of manipulation or synthetic generation were found.",
    "likely-false": "The content contains claims that contradict multiple credible sources.",
    "likely-true": "The content is consistent with information from verified sources.",
    unverified: "Insufficient data to reach a definitive conclusion. Further review is recommended.",
  };

  const verdictLabels: Record<typeof verdict, string> = {
    "ai-generated": "AI-Generated / Manipulated",
    authentic: "Appears Authentic",
    "likely-false": "Likely Misinformation",
    "likely-true": "Likely Credible",
    unverified: "Unverified",
  };

  return {
    text: `Analysis complete. Verdict: **${verdictLabels[verdict]}** (${confidence}% confidence). ${summaries[verdict]}`,
    analysis: {
      verdict,
      confidence,
      riskLevel: riskMap[verdict],
      summary: summaries[verdict],
      details: [
        { label: "Content type detected", value: ["Text / article", "Image", "Video clip", "Social media post", "URL"][rand(0, 4)], flag: "info" },
        { label: "Synthetic media indicators", value: verdict === "ai-generated" ? "Present" : "None detected", flag: verdict === "ai-generated" ? "warn" : "ok" },
        { label: "Claim verification", value: verdict === "likely-false" ? "Contradicted by sources" : verdict === "likely-true" ? "Corroborated" : "Inconclusive", flag: verdict === "likely-false" ? "warn" : verdict === "likely-true" ? "ok" : "info" },
        { label: "Source credibility", value: ["High", "Medium", "Low", "Unknown"][rand(0, 3)], flag: "info" },
        { label: "Flagged by fact-checkers", value: verdict === "likely-false" ? `${rand(1, 4)} records found` : "None", flag: verdict === "likely-false" ? "warn" : "ok" },
      ],
    },
  };
}

