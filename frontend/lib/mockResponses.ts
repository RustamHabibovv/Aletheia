function factCheckResponse(input: string): { text: string; analysis: AnalysisResult } {
  const outcomes = ["likely-false", "likely-true", "unverified"] as const;
  const verdict = outcomes[Math.floor(Math.random() * outcomes.length)];
  const confidence = rand(60, 94);
  const verdictLabels = { "likely-false": "Likely False", "likely-true": "Likely True", unverified: "Unverified" };
  const riskMap = { "likely-false": "high", "likely-true": "low", unverified: "medium" } as const;

  return {
    text:
      verdict === "likely-false"
        ? `Fact-check complete. This claim is rated **"${verdictLabels[verdict]}"** (${confidence}% confidence). Cross-referencing with ${rand(3, 8)} trusted sources found contradicting evidence.`
        : verdict === "likely-true"
        ? `Fact-check complete. This claim is rated **"${verdictLabels[verdict]}"** (${confidence}% confidence). Multiple reliable sources corroborate this statement.`
        : `Fact-check complete. This claim is **"${verdictLabels[verdict]}"** — insufficient evidence found to confirm or deny it.`,
    analysis: {
      verdict,
      confidence,
      riskLevel: riskMap[verdict],
      summary:
        verdict === "likely-false"
          ? "Multiple credible sources contradict this claim. It may be misleading or fabricated."
          : verdict === "likely-true"
          ? "The claim is consistent with reporting from several high-reliability sources."
          : "Available sources do not provide enough information to conclusively verify this claim.",
      details: [
        {
          label: "Sources checked",
          value: `${rand(4, 12)} databases and news archives`,
          flag: "info",
        },
        {
          label: "Matching fact-check records",
          value: verdict === "unverified" ? "0 direct matches" : `${rand(2, 6)} matches found`,
          flag: verdict === "unverified" ? "warn" : verdict === "likely-false" ? "warn" : "ok",
        },
        {
          label: "Claim category",
          value: ["Health", "Politics", "Science", "Economy", "Technology"][rand(0, 4)],
          flag: "info",
        },
        {
          label: "First seen online",
          value: `${rand(1, 18)} months ago`,
          flag: "info",
        },
      ],
      sources:
        verdict !== "unverified"
          ? [
              { title: "Reuters Fact Check", url: "#", reliability: "high" },
              { title: "AP Fact Check", url: "#", reliability: "high" },
              { title: "Snopes", url: "#", reliability: "medium" },
            ]
          : [],
    },
  };
}

